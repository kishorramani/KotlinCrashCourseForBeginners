package com.kishorramani


//Primitive Data Type: Int, Float, Doubt, Boolean, String
fun main() {
    println("========== val =========")
    val x = 7       //Assignment operator
    println(x)

    val x1: Int = 11        //Add data type
    println(x1)

    val x2: Float = 12.223f     //32 bit, it's required less memory
    println(x2)

    val x3: Double = 12.124     //64 bit
    println(x3)

    val x4 = true
    println(x4)

    val x5 = "This is x5"
    println(x5)

    println("========== var =========")

    var x6 = "Hello Kotliers"
    x6 = "Let's help each other"
    println(x6)
}
